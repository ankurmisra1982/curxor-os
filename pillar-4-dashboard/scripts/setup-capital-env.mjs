#!/usr/bin/env node
/**
 * Bootstrap local Capital Claw env: digital.env + .env.local pointer.
 *
 * Usage: node scripts/setup-capital-env.mjs
 */
import { copyFileSync, existsSync, readFileSync, writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const DASHBOARD = path.resolve(__dirname, "..");
const DEV_QA = path.join(DASHBOARD, "scripts", "dev-qa");
const DIGITAL_EXAMPLE = path.join(DEV_QA, "digital.env.example");
const DIGITAL_ENV = path.join(DEV_QA, "digital.env");
const ENV_LOCAL = path.join(DASHBOARD, ".env.local");
const ENV_LOCAL_EXAMPLE = path.join(DASHBOARD, ".env.local.example");

const devQaPaths = `
CURXOR_FRE_STATE_PATH=${path.join(DEV_QA, "fre-state.json")}
CURXOR_USER_SETTINGS_PATH=${path.join(DEV_QA, "user-settings.json")}
CURXOR_CAPITAL_QUEUE_PATH=${path.join(DEV_QA, "capital-queue.json")}
CURXOR_CAPITAL_PFM_PATH=${path.join(DEV_QA, "capital-pfm.json")}
CURXOR_CAPITAL_INTEL_PATH=${path.join(DEV_QA, "capital-intel.json")}
CURXOR_CAPITAL_PLAID_PATH=${path.join(DEV_QA, "capital-plaid.json")}
CURXOR_CAPITAL_SNAPTRADE_PATH=${path.join(DEV_QA, "capital-snaptrade.json")}
CURXOR_DIGITAL_ENV_PATH=${DIGITAL_ENV}
CURXOR_FAMILY_PROFILES_PATH=${path.join(DEV_QA, "family-profiles.json")}
CURXOR_APP_FRE_DIR=${path.join(DEV_QA, "app-fre")}
CURXOR_VITAL_STATE_PATH=${path.join(DEV_QA, "vital-health.json")}
CURXOR_CLAW_CONTEXT_PATH=${path.join(DEV_QA, "claw-context.json")}
CURXOR_HUMANOID_HUB_PATH=${path.join(DEV_QA, "humanoid-hub.json")}
`.trim();

function main() {
  if (!existsSync(DIGITAL_ENV)) {
    copyFileSync(DIGITAL_EXAMPLE, DIGITAL_ENV);
    console.log(`Created ${DIGITAL_ENV}`);
    console.log("  → Add Alpaca, Plaid, and SnapTrade keys from provider dashboards.\n");
  } else {
    console.log(`Exists ${DIGITAL_ENV}\n`);
  }

  const localBlock = `# CurXor local dev — generated by setup-capital-env.mjs
${devQaPaths}
PORT=3080
HOSTNAME=127.0.0.1
`;

  if (!existsSync(ENV_LOCAL)) {
    writeFileSync(ENV_LOCAL, localBlock, "utf8");
    console.log(`Created ${ENV_LOCAL}`);
  } else {
    let content = readFileSync(ENV_LOCAL, "utf8");
    if (!content.includes("CURXOR_DIGITAL_ENV_PATH")) {
      writeFileSync(ENV_LOCAL, `${content.trim()}\n\n${localBlock}`, "utf8");
      console.log(`Appended Capital paths to ${ENV_LOCAL}`);
    } else {
      console.log(`${ENV_LOCAL} already points at digital.env`);
    }
  }

  if (!existsSync(ENV_LOCAL_EXAMPLE)) {
    writeFileSync(ENV_LOCAL_EXAMPLE, `${localBlock}# Copy to .env.local for npm run dev\n`, "utf8");
    console.log(`Created ${ENV_LOCAL_EXAMPLE}`);
  }

  console.log("\nNext steps:");
  console.log("  Demo mode: no keys required — npm run dev and open /my-capital");
  console.log("  When ready for brokers: edit scripts/dev-qa/digital.env (see docs/capital-claw/RELEASE-NEXT.md)");
  console.log("  npm run dev  (or npm run qa:local)\n");
}

main();
