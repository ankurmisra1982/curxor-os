#!/usr/bin/env node
/**
 * Bootstrap local Work Claw env: digital.env + .env.local pointer.
 *
 * Usage: node scripts/setup-work-env.mjs
 */
import { copyFileSync, existsSync, readFileSync, writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const DASHBOARD = path.resolve(__dirname, "..");
const REPO = path.resolve(DASHBOARD, "..");
const DEV_QA = path.join(DASHBOARD, "scripts", "dev-qa");
const DIGITAL_EXAMPLE = path.join(REPO, "config", "digital", "digital.env.example");
const DIGITAL_ENV = path.join(DEV_QA, "digital.env");
const ENV_LOCAL = path.join(DASHBOARD, ".env.local");

const devQaPaths = `
CURXOR_FRE_STATE_PATH=${path.join(DEV_QA, "fre-state.json")}
CURXOR_USER_SETTINGS_PATH=${path.join(DEV_QA, "user-settings.json")}
CURXOR_WORK_QUEUE_PATH=${path.join(DEV_QA, "work-queue.json")}
CURXOR_PROVIDER_LINK_SESSIONS_PATH=${path.join(DEV_QA, "provider-link-sessions.json")}
CURXOR_DIGITAL_ENV_PATH=${DIGITAL_ENV}
`.trim();

function main() {
  if (!existsSync(DIGITAL_ENV)) {
    copyFileSync(DIGITAL_EXAMPLE, DIGITAL_ENV);
    console.log(`Created ${DIGITAL_ENV}`);
    console.log("  → Add SMTP, IMAP, Google, and Twenty keys when ready for live comms.\n");
  } else {
    console.log(`Exists ${DIGITAL_ENV}\n`);
  }

  const localBlock = `# CurXor local dev — generated by setup-work-env.mjs
${devQaPaths}
PORT=3080
HOSTNAME=127.0.0.1
`;

  if (!existsSync(ENV_LOCAL)) {
    writeFileSync(ENV_LOCAL, localBlock, "utf8");
    console.log(`Created ${ENV_LOCAL}`);
  } else {
    let content = readFileSync(ENV_LOCAL, "utf8");
    if (!content.includes("CURXOR_WORK_QUEUE_PATH")) {
      writeFileSync(ENV_LOCAL, `${content.trim()}\n\n${localBlock}`, "utf8");
      console.log(`Appended Work paths to ${ENV_LOCAL}`);
    } else {
      console.log(`${ENV_LOCAL} already points at work-queue.json`);
    }
  }

  console.log("\nNext steps:");
  console.log("  Demo mode: no keys required — npm run dev and open /my-work");
  console.log("  Exit demo: docs/outreach-claw/EXIT-DEMO.md");
  console.log("  npm run verify:work-exit-demo-scaffold\n");
}

main();
